package com.example.model;

/**
 * Enum representing different user roles in the cinema application.
 * <p>
 * This enum defines three possible user roles:
 * <ul>
 *     <li>{@link #CASHIER} - A cashier who handles ticket sales.</li>
 *     <li>{@link #ADMIN} - An administrator with full access to the system.</li>
 *     <li>{@link #MANAGER} - A manager who oversees cinema operations.</li>
 * </ul>
 */
public enum Role {
    CASHIER,
    ADMIN,
    MANAGER
}
